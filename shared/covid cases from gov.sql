FROM #COVIDCases c
	LEFT OUTER JOIN #COVIDCasesFromGMCR g ON g.Commissioner = c.CCGId AND g.Date = c.Date
ORDER BY c.CCGName, c.Date;

-- Output the total number per CCG up to the end of September
