  c.CCGName, SUM(c.DailyCases) AS OfficialTotalCases, SUM(g.DailyCases) AS GMCRTotalCases, 
  100* CAST(SUM(g.DailyCases) AS float) / SUM(c.DailyCases) AS [Percentage]
FROM #COVIDCases c
	LEFT OUTER JOIN #COVIDCasesFromGMCR g ON g.Commissioner = c.CCGId AND g.Date = c.Date
