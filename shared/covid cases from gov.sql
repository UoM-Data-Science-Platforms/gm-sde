-- Find the first positive test for each patient. Baseline assumption is that
-- people typically only get it once (newsworthy if not). So it's far more likely
-- to be a problem if we count patients multiple times.
IF OBJECT_ID('tempdb..#COVIDPatientFirstDiagnosis') IS NOT NULL DROP TABLE #COVIDPatientFirstDiagnosis;
SELECT c.FK_Patient_Link_ID, MIN(CONVERT(DATE, EventDate)) as [Date] INTO #COVIDPatientFirstDiagnosis
FROM RLS.vw_COVID19 c
  INNER JOIN RLS.vw_Patient p ON p.FK_Patient_Link_ID = c.FK_Patient_Link_ID
WHERE FK_Reference_Tenancy_ID = 2
  -- If looking for tests rather than positive cases can use 'Tested' and 'Tested for immunity'
  -- but we just want confirmed cases at the moment.
  AND GroupDescription IN ('Confirmed') 
GROUP BY c.FK_Patient_Link_ID;

-- Populate a temp table from the GMCR with number of cases per day for each CCG
IF OBJECT_ID('tempdb..#COVIDCasesFromGMCR') IS NOT NULL DROP TABLE #COVIDCasesFromGMCR;
SELECT Commissioner, [Date], count(*) as [DailyCases] INTO #COVIDCasesFromGMCR
FROM #COVIDPatientFirstDiagnosis c
  INNER JOIN RLS.vw_Patient p ON p.FK_Patient_Link_ID = c.FK_Patient_Link_ID
  INNER JOIN SharedCare.Reference_GP_Practice g ON g.OrganisationCode = p.GPPracticeCode
WHERE FK_Reference_Tenancy_ID = 2
GROUP BY Commissioner, [Date]
ORDER BY Commissioner, [Date]

